public class ForEmArray {
    public static void main(String[] args) {
        
        String alunos[] = {"Ana", "Alberto", "Rafaella", "Zuleide"};

        for (int i = 0; i < alunos.length; i++){
            System.out.println("O aluno de índice x = " + i + " é " + alunos[i]);
        }
        
    }
}